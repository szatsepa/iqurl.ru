function attachData(cod, str) {
    
    var obj = document.getElementById(cod);
	
    alert(obj.value+'\n'+str);
	
return;
}

function preload(id){
    
        var obj = document.getElementById(id);

        obj.style.display = "inline-block";
        
}   

